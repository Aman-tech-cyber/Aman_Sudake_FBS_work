##Comparision operators is also known as Reletional Operators
#1. ==  Equal to
print(10 =='20')

#2. !=  Not equal to
print(10 != 20)

#3. >   Greater than
print('a'>'b')
print(10>5)
print(10 > 20)
#print(10 > 'a') # Error

#4. >=   Greater than or equal to
print(10 >= 10)
print(10 >= 5)

#5. <   Less than
print(5 < 10)

#6. <=   Less than or equal to
print(5 <= 5)