#1. & (and): consider 1 if both bits are 1 otherwise 0
print(13 & 6)

#2. | (OR) : consider 0 if both bits are 0 otherwise 1
print(13 | 6)

#3. ^ (XOR) symbol uppercap : Consider 1 for different bits, 0 for same
print(13 ^ 6)

#4. ~ (NOT): ~x = - (x+1)
print(~10)

#5. << (left shift)  (symbol angular brackets):according to the give value the bits will be move left side    
print(13 << 2)  #(13 << value)

#6. >> (right shift):sccording to the give value the bits will be move right side 
print(13 >> 2)
